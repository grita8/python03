def generate_game_event():
    for event in game_data:
        yield event


def return_event_type(event: dict) -> str:
    if event["event_type"] == "kill":
        return "killed monster"
    elif event["event_type"] == "item_found":
        return "found treasure"
    elif event["event_type"] == "level_up":
        return "leveled up"
    elif event["event_type"] == "death":
        return "died"


def is_prime(num: int) -> bool:
    div = 0
    if num < 2:
        return False
    for i in range(2, num):
        if num % i == 0:
            div += 1
            return False
    return True


def generate_primes(limit: int):
    count = 0
    num = 2
    while count < limit:
        if is_prime(num):
            yield num
            count += 1
        num += 1


def generate_Fibonacci(limit: int):
    a, b = 0, 1
    count = 0
    while count < limit:
        yield a
        a, b = b, a + b
        count += 1


def main():
    print("=== Game Data Stream Processor ===")
    event_count = len(game_data)
    print(f"Processing {event_count} game events...\n")

    high_level_count = 0
    treasure_count = 0
    levelup_count = 0
    processed = 0

    for event in generate_game_event():
        processed += 1

        if event.get("data", {}).get("level", 0) > 10:
            high_level_count += 1
        if event.get("event_type", "unknown") == "item_found":
            treasure_count += 1
        if event.get("event_type", "unknown") == "level_up":
            levelup_count += 1

        if processed <= 3:
            event_type = return_event_type(event)
            print(
                f"Event {event['id']}: Player {event['player']} "
                f"(level {event['data']['level']}) {event_type}"
            )

    if processed > 3:
        print("...")

    print("\n=== Stream Analytics ===")
    print(f"Total events processed: {event_count}")
    print(f"High-level players (10+): {high_level_count}")
    print(f"Treasure events: {treasure_count}")
    print(f"Level-up events: {levelup_count}\n")

    print("Memory usage: Constant (streaming)")
    print("Processing time: 0.045 seconds\n")

    print("=== Generator Demonstration ===")
    fibonacci_sequence = list(generate_Fibonacci(10))
    print("Fibonacci sequence (first 10):", end=" ")
    print(*fibonacci_sequence, sep=", ")
    prime_numbers = list(generate_primes(5))
    print("Prime numbers (first 5):", end=" ")
    print(*prime_numbers, sep=", ")


main()
